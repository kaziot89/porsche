orderButton.addEventListener("click", function () {
  const nameValue = inputName.value;

  if (nameValue.split(" ").length === 2) {
    const clientName = localStorage.getItem("clientName");
    const nameElement = document.getElementById("nameStorage");
    nameElement.textContent = clientName;
  } else {
    alert("Wprowadź poprawne imię i nazwisko");
    return;
  }
  const mailValue = inputMail.value;
  const emailRegExp = /^[^\s@]+@[^\s@]+$/;
  if (emailRegExp.test(mailValue)) {
    const clientMail = localStorage.getItem("clientMail");
    const mailElement = document.getElementById("emailStorage");
    mailElement.textContent = clientMail;
  } else {
    alert("Wprowadź poprawny adres e-mail.");
    return;
  }
  const selectLocation = document.getElementById("location");
  const selectedLocation = selectLocation.value;
  if (selectedLocation === "") {
    alert("Proszę wybrać miasto odbioru.");
    return;
  }
  const selectDate = document.getElementById("date");
  const selectedDate = selectDate.value;
  if (selectedDate === "") {
    alert("Proszę wyznaczyć datę odbioru.");
    return;
  }

  // Ukryj div z zamówieniem i wyświetl div z potwierdzeniem zamówienia
  orderContainer.style.display = "none";
  orderCompleted.style.display = "block";
  congratsContainer.style.display = "block";
  submitBtns.style.display = "none";

  const financing = localStorage.getItem("financing") || "";
  const financingElement = document.getElementById("financingMethod");
  financingElement.textContent = financing;

  // Pobierz cenę z Local Storage i wyświetl ją w div z potwierdzeniem zamówienia
  const add1 = localStorage.getItem("optionalWheels");
  const wheelsElement = document.getElementById("add1");
  wheelsElement.textContent = add1;

  const add2 = localStorage.getItem("optionalGuarantee");
  const guaranteeElement = document.getElementById("add2");
  guaranteeElement.textContent = add2;

  const location = localStorage.getItem("clientLocation");
  const locationElement = document.getElementById("locationStorage");
  locationElement.textContent = location;

  const date = localStorage.getItem("dateOfReceipt");
  const dateElement = document.getElementById("dateStorage");
  dateElement.textContent = date;

  clearInput();
});
document.addEventListener("DOMContentLoaded", function () {
  window.addEventListener("scroll", function () {
    var images = document.getElementsByClassName("bounce");
    var halfPageHeight = window.innerHeight / 100;
    var scrollPosition = window.scrollY || window.pageYOffset;

    for (var i = 0; i < images.length; i++) {
      var image = images[i];

      if (scrollPosition > halfPageHeight) {
        image.style.visibility = "hidden";
      } else {
        image.style.visibility = "visible";
      }
    }
  });
});
